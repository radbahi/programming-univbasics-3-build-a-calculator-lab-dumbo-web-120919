def addition(num1, num2)
result = num1 + num2
result
end

def subtraction(num1, num2)
result = num1 - num2
result
end

def division(num1, num2)
  result = num1 / num2
  result
end

def multiplication(num1, num2)
  result = num1 * num2
  result
end

def modulo(num1, num2)
num1.remainder(num2)
end

def square_root(num)
Math.sqrt(num)
end
